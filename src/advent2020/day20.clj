(defn edges
  [{:keys [grid]}]
  (let [edge-coords [(map vector (repeat 0) (range 10))
                     (map vector (repeat 9) (range 10))
                     (map vector (range 10) (repeat 0))
                     (map vector (range 10) (repeat 9))]]
    (map (partial map grid) edge-coords)))

(def day20-input
  (->> (u/puzzle-input "day20-input.txt")
       (str/join "\n")
       parse))